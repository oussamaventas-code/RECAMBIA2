export function getProductBySlug(slug: string): Product | undefined {
  return products.find((p) => p.slug === slug);
}

export function getProductsByIds(ids: string[]): Product[] {
  return ids
    .map((id) => products.find((p) => p.id === id))
    .filter((p): p is Product => Boolean(p));
}
export function searchProducts(query: string): Product[] {
  if (!query || query.trim() === "") return [];

  const lowerQuery = query.toLowerCase().trim();

  return products.filter((p) => {
    // Buscar en nombre o marca
    if (p.name.toLowerCase().includes(lowerQuery) || p.brand.toLowerCase().includes(lowerQuery)) return true;

    // Buscar en categoría
    if (p.category.toLowerCase().includes(lowerQuery)) return true;

    // Buscar en referencias cruzadas (limpiando espacios para coincidencias más flexibles)
    const cleanQuery = lowerQuery.replace(/\s+/g, "");

    if (p.oemRef?.toLowerCase().replace(/\s+/g, "").includes(cleanQuery)) return true;

    if (p.equivalentRefs?.some(ref => ref.toLowerCase().replace(/\s+/g, "").includes(cleanQuery))) return true;

    // Buscar en compatibilidad
    if (p.compatibleWith?.some(car => car.toLowerCase().includes(lowerQuery))) return true;

    return false;
  });
}
